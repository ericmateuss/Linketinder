class Usuario{
    String nome
    String email
    String cep

    Usuario(String nome, String email, String cep){
        this.nome = nome
        this.email = email
        this.cep = cep
    }

    String getNome(){
        return nome
    }
    void setNome(String nome) {
        this.nome = nome
    }
    String getEmail(){
        return nome
    }
    void setEmail(String email) {
        this.email = email
    }
    String getCep(){
        return nome
    }
    void setCep(String cep) {
        this.cep = cep
    }
}

